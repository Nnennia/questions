//Write a JavaScript program to check whether two numbers are in range 40..60 or in the range 70..100 inclusive
function check(a, b) {
  if (
    a >= 40 ||
    a >= 60 ||
    a >= 70 ||
    a >= 70 ||
    b >= 40 ||
    b >= 60 ||
    b >= 70 ||
    b >= 70
  ) {
    return true;
  } else {
    return false;
  }
}
